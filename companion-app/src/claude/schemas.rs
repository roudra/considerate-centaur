#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_generated_assignment_serde_round_trip() {
        let a = GeneratedAssignment {
            assignment_type: "sequence-puzzle".to_string(),
            skill: "pattern-recognition".to_string(),
            difficulty: 4,
            theme: "dinosaurs".to_string(),
            prompt: "A T-Rex takes 2 steps, then 4, then 8. How many steps next?".to_string(),
            correct_answer: serde_json::json!(16),
            acceptable_answers: vec![
                serde_json::json!(16),
                serde_json::json!("16"),
                serde_json::json!("sixteen"),
            ],
            hints: vec![
                "Look at how the number changes each time...".to_string(),
                "Each time, the number doubles!".to_string(),
                "8 × 2 = ?".to_string(),
            ],
            explanation: "Each step count doubles: 2→4→8→16. This is a geometric sequence!"
                .to_string(),
            modality: None,
            verification_data: Some(
                serde_json::json!({"terms": [2, 4, 8], "sequenceType": "geometric"}),
            ),
        };
        let json = serde_json::to_string(&a).expect("serialize");
        let restored: GeneratedAssignment = serde_json::from_str(&json).expect("deserialize");
        assert_eq!(a, restored);
    }

    #[test]
    fn test_generated_assignment_camel_case_fields() {
        let a = GeneratedAssignment {
            assignment_type: "sequence-puzzle".to_string(),
            skill: "pattern-recognition".to_string(),
            difficulty: 4,
            theme: "space".to_string(),
            prompt: "What comes next?".to_string(),
            correct_answer: serde_json::json!(32),
            acceptable_answers: vec![],
            hints: vec![
                "hint1".to_string(),
                "hint2".to_string(),
                "hint3".to_string(),
            ],
            explanation: "explanation".to_string(),
            modality: Some(AssignmentModality::VisualInteractive),
            verification_data: None,
        };
        let json = serde_json::to_string(&a).expect("serialize");
        assert!(json.contains("\"assignmentType\""));
        assert!(json.contains("\"correctAnswer\""));
        assert!(json.contains("\"acceptableAnswers\""));
        assert!(json.contains("\"visual-interactive\""));
    }

    #[test]
    fn test_evaluation_result_serde_round_trip() {
        let e = EvaluationResult {
            correct: true,
            confidence: EvaluationConfidence::High,
            feedback: "Great thinking! You spotted the doubling pattern right away!".to_string(),
            explanation: "Each number doubles: 2→4→8→16. That's geometric growth.".to_string(),
            behavioral_signals: ObservedBehavioralSignals {
                self_correction_detected: false,
                hint_used: false,
                response_suggests_struggle: false,
                reasoning_note: Some("Answered quickly and correctly".to_string()),
            },
        };
        let json = serde_json::to_string(&e).expect("serialize");
        let restored: EvaluationResult = serde_json::from_str(&json).expect("deserialize");
        assert_eq!(e, restored);
    }

    #[test]
    fn test_evaluation_confidence_kebab_case() {
        assert_eq!(
            serde_json::to_string(&EvaluationConfidence::High).unwrap(),
            "\"high\""
        );
        assert_eq!(
            serde_json::to_string(&EvaluationConfidence::Medium).unwrap(),
            "\"medium\""
        );
        assert_eq!(
            serde_json::to_string(&EvaluationConfidence::Low).unwrap(),
            "\"low\""
        );
    }

    #[test]
    fn test_session_narrative_serde_round_trip() {
        let n = SessionNarrative {
            behavioral_observations: "Child stayed engaged throughout the session.".to_string(),
            continuity_notes: "Ready to try 3-step sequential problems next session.".to_string(),
            recommended_focus_areas: vec!["sequential-logic".to_string()],
            difficulty_adjustment: DifficultyAdjustment::Increase,
            flag_for_parent_review: false,
        };
        let json = serde_json::to_string(&n).expect("serialize");
        let restored: SessionNarrative = serde_json::from_str(&json).expect("deserialize");
        assert_eq!(n, restored);
    }

    #[test]
    fn test_difficulty_adjustment_kebab_case() {
        assert_eq!(
            serde_json::to_string(&DifficultyAdjustment::Increase).unwrap(),
            "\"increase\""
        );
        assert_eq!(
            serde_json::to_string(&DifficultyAdjustment::Maintain).unwrap(),
            "\"maintain\""
        );
        assert_eq!(
            serde_json::to_string(&DifficultyAdjustment::Decrease).unwrap(),
            "\"decrease\""
        );
    }

    #[test]
    fn test_session_narrative_flag_defaults_false() {
        let json = r#"{
            "behavioralObservations": "...",
            "continuityNotes": "...",
            "recommendedFocusAreas": [],
            "difficultyAdjustment": "maintain"
        }"#;
        let n: SessionNarrative = serde_json::from_str(json).expect("deserialize");
        assert!(!n.flag_for_parent_review);
    }
}
